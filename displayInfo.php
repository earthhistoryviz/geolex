    <h3><b>Type Locality and Naming</b></h3>
    <hr>
    <p>The type section is located at Amunik Mt., southeast of Xitieshan and southwest of Delingha, in the northern margin of Qaidam Basin, Qinghai Province. It was named by Qinghai No 1 Team of Regional Geological Survey in 	1976</p>
    <br>
    <h3><b>Lithology and Thickness</b></h3>
    <hr>
    <p>Sandstone, conglomerate. The formation is mainly characterized by purple, grayish-purple, gray, yellowish-pink arkosic quartzose sandstone and silty mudstone. At the bottom is a bed of purple conglomerate about 2 m thick. The composition of the conglomerate is mainly chert, volcanic rock and limestone, with the pebbles being 2-4 cm in diameter, angular to subangular in shape and cemented with sands.<br>
In Amunik section, the formation is 116 m in thickness.</p>
